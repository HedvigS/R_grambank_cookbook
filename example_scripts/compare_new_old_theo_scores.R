# This script showcases how to use the function make_theo_scores to make theoretical scores from Grambank data, such as fusion, informativity etc.
# The script also compares the approach of Hedvigs/R_grambank_cookbook to that of grambank/grambank-analysed (release paper).
# In grambank/grambank-analysed we did a subsetting of the entire dataset where we pruned away features and languages with large amounts of missing data. We then used this subset in several parts of the analysis, including PCA and calculation of theoretical scores. The function make_theo_scores in R_scripts/make_theo_scores.R instead prunes for missing data with respect to the specific features involved in each of the theoretical scores. Furthermore, the function allows the users to set a different cut-off (default = 0.75). The difference is very small in practice, as will be evident by the plots generated by this script. Please see the following for more details: https://github.com/HedvigS/rgrambank?tab=readme-ov-file#theoretical-scores
# This example uses rcldf to fetch and parse Grambank v1.0.3 from Zenodo's online webplatform. If you already have Grambank downloaded locally, feel free to change the line which calls rcldf::cldf to point to the JSON-file instead. An example of this is provided below in a line that is commented out.
# This script specifies particular versions of the packages tidyverse and rcldf to increase replicability.

